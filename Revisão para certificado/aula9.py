frase = '   Curso    em    Vídeo    Python   '
print(frase[:5])#fatiamento sem inicio até o 5
print(frase[6:8])#//
print(frase[9:14])#//
print(frase[15:])#//
print(len(frase))#quantos caracteres tem na frase
print(frase.upper())#deixar tudo em maisculo
print(frase.lower())#deixar tudo em minusculo
print(frase.count('o'))#contar quantas letras o
print(frase.find('h'))#encontra e indicar em qual caractere começa
print(frase.replace('em', 'de'))#trocar partes
print(frase.title())#deixar todas as primeiras letra de cada palavra em maisculo
print(frase.capitalize())#deixar
print(frase.strip())#apagar espaços antes e depois
print(frase.split())#separar em lista
print(''.join(frase))#juntar palavras antes separadas
